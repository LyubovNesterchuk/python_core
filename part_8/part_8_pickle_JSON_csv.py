'''Python надає декілька модулів для серіалізації, найпопулярнішими з яких є pickle і json.'''


'''Модуль pickle дозволяє серіалізувати об'єкти Python у потік байтів та десеріалізувати 
потік байтів назад у об'єкти. Це виправдано та корисно для зберігання об'єктів у файлах
або передачі даних через мережу.
Основна мета pickle — зберегти стан об'єкта так, щоб його можна було точно відновити пізніше
і часто в іншому місці.

У пакета pickle є дві пари парних методів:

Метод dumps запаковує в byte-рядок об'єкт, а метод loads потім розпаковує назад 
з byte-рядка в об'єкт. Ці методи потрібні, коли ми хочемо контролювати, що робити 
з byte представленням, наприклад, відправити його мережею або прийняти з мережі.
'''
import pickle

my_data = {"key": "value", "num": 42}# Об'єкт для серіалізації

serialized_data = pickle.dumps(my_data)# Серіалізація об'єкта в байтовий рядок
# Виведе байтовий рядок
print(serialized_data)  # b'\\x80\\x04\\x95\\x1b\\x00\\x00\\x00\\x00\\x00\\x00\\x00}\\x94(\\x8c\\x03key\\x94\\x8c\\x05value\\x94\\x8c\\x03num\\x94K*u.'

deserialized_data = pickle.loads(serialized_data)# Десеріалізація об'єкта з байтового рядка
# Виведе вихідний об'єкт Python
print(deserialized_data) #{'key': 'value', 'num': 42}


'''методи dump та load, що упаковують дані у файл та розпаковують із файлу'''

with open("data.pickle", "wb") as file: # Серіалізація об'єкта в файл
    pickle.dump(my_data, file)

with open('data.pickle', 'rb') as file: # Десеріалізація об'єкта з файлу
    deserialized_data = pickle.load(file)
# Виведе вихідний об'єкт Python
print(deserialized_data) #{'key': 'value', 'num': 42}


'''Самі класи та функції pickle зберігати не вміє і, якщо вам потрібно розпакувати 
упакований об'єкт класу, то сам клас повинен бути оголошений раніше у коді.'''

class Human:
    def __init__(self, name):
        self.name = name

bob = Human("Bob")
with open("instance.pickle", "wb") as file:
    pickle.dump(bob, file)

'''Важливо, щоб клас Human був визначений у скрипті, який виконує десеріалізацію, 
із тією ж структурою та в тому ж просторі імен, що й у скрипті, який виконав серіалізацію.

Якщо при десеріалізації не вказати клас Human ми отримаємо помилку.
'''

class Human:
    def __init__(self, name):
        self.name = name

with open("instance.pickle", "rb") as file:
    loaded_instance = pickle.load(file)

print(loaded_instance.name) #Bob


'''Одна із розповсюджених практик це збереження налаштувань програми. Якщо ваша програма 
дозволяє користувачам налаштовувати різні параметри, ви можете серіалізувати ці налаштування
 в файл і десеріалізувати їх при наступному запуску програми для користувача.'''

# Збереження налаштувань
settings = {'theme': 'dark', 'language': 'ukrainian'}
