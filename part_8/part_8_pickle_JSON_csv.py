'''CSV (Comma-Separated Values) — формат файлу, що використовується для зберігання табличних даних. 
Його назва походить від основної ідеї - використання коми для відділення значень в середині рядка.
Однак на практиці як роздільник можуть використовуватися й інші символи, наприклад крапка з комою ;
або знак табуляції. 
Файл CSV складається з рядків, і кожен рядок представляє один запис. Записи поділяються на поля, 
які розділені зазвичай комами. Перший рядок у файлі часто використовується для заголовків, 
що описують зміст кожного поля у записах.
'''

'''Різні операційні системи використовують різні символи для позначення кінця рядка в 
текстових файлах: Windows використовує повернення каретки та переведення рядка \r\n, 
Linux та MacOS використовує лише переведення рядка \n
Коли ми відкриваємо файл для читання чи запису, параметр newline='' інструктує Python 
не проводити жодної спеціальної обробки символів кінця рядка. 
'''
'''Для запису даних у CSV файл можна використати функцію csv.writer. '''
import csv

# Дані для запису
rows = [
    ["name", "age", "specialty"],
    ["Василь Гупало", 30, "Математика"],
    ["Марія Петренко", 22, "Фізика"],
    ["Олександр Коваленко", 20, "Інформатика"],
]


with open("data.csv", "w", newline="", encoding="utf-8") as csvfile: # Відкриваємо файл для запису
    writer = csv.writer(csvfile, delimiter=",")       # Створюємо об'єкт writer
    writer.writerows(rows)                            # Записуємо рядки даних


'''Для читання даних з CSV файлу можна використовувати функцію csv.reader, що повертає об'єкт, 
який ітерує по рядках файлу.'''

with open("data.csv", newline="", encoding="utf-8") as csvfile:
    reader = csv.reader(csvfile, delimiter=",")    # Створюємо об'єкт reader
    for row in reader:                             # Проходимося по кожному рядку у файлі
        print(", ".join(row))

'''name, age, specialty
Василь Гупало, 30, Математика
Марія Петренко, 22, Фізика
Олександр Коваленко, 20, Інформатика
'''  

'''За допомогою writer.writerows(rows) можна записати кілька рядків одразу. 
Якщо потрібно записати один рядок, можна використати writer.writerow(row).

використання DictReader і DictWriter полегшує доступ до полів за їхніми назвами 
та автоматизує процес запису заголовків стовпців.
'''

# Запис у CSV файл зі словників
with open("students.csv", "w", newline="", encoding="utf-8") as csvfile:
    fieldnames = ["name", "age", "specialty"]
    writer = csv.DictWriter(csvfile, fieldnames=fieldnames)

    writer.writeheader()
    writer.writerow({"name": "Олег Олегов", "age": 23, "specialty": "Історія"})
    writer.writerow({"name": "Анна Сергіївна", "age": 22, "specialty": "Біологія"})

# Читання з CSV файлу в словники
with open("students.csv", newline="", encoding="utf-8") as csvfile:
    reader = csv.DictReader(csvfile)
    for row in reader:
        print(row["name"], row["age"], row["specialty"])

'''name, age, specialty
Василь Гупало, 30, Математика
Марія Петренко, 22, Фізика'''

