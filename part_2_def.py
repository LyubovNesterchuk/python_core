'''Змінна кількість параметрів

Параметр *args. Він дозволяє функції приймати довільну кількість позиційних аргументів.
Аргументи, передані функції, зберігаються у вигляді кортежу.

Параметр **kwargs (keyword arguments) Він дозволяє функції приймати довільну кількість ключових аргументів. 
Але аргументи, передані функції, зберігаються вже у вигляді словника.

коли ви використовуєте обидва ці параметри разом, *args має йти перед **kwargs

всі позиційні аргументи, передані після останнього визначеного аргументу,
будуть упаковані в кортеж (словник) під ім'ям,
що йде після символу * (**) і зазвичай це ім'я args (kwargs), але може бути будь-яке інше ім'я
'''

def print_all_args(*args):
    for arg in args:
        print(arg)

print_all_args(1, 'hello', True)



def concatenate(*args) -> str:
    result = ""
    for arg in args:
        result += arg
    return result

print(concatenate("Hello", " ", "world", "!"))



def greet(**kwargs):
    for key, value in kwargs.items():
        print(f"{key}: {value}")

greet(name="Alice", age=25) # {'name': 'Alice', 'age': 25}


def example_function(*args, **kwargs):
    print("Позиційні аргументи:", args)
    print("Ключові аргументи:", kwargs)

example_function(1, 2, 3, name="Alice", age=25) # Позиційні аргументи: (1, 2, 3) Ключові аргументи: {'name': 'Alice', 'age': 25}


