'''Рекурсія — це концепція в програмуванні, коли функція викликає саму себе в рамках власного виконання.
Це схоже на дроблення великої проблеми на менші, керованіші проблеми.

обчислення факторіала числа або чисел Фібоначчі

основні компоненти рекурсії:
Базовий випадок: Це умова, при якій рекурсія припиняє виклик самої себе, 
щоб уникнути нескінченного циклу. Він важливий для запобігання нескінченному циклу в рекурсії.
Рекурсивний випадок: Це умова, за якої функція викликає саму себе з новими аргументами.
Фактично це ситуація, коли функція продовжує викликати саму себе, розбиваючи проблему на менші частини

Якщо рекурсивні виклики функції продовжуються без досягнення базового випадку, 
стек викликів може переповнитись, що призведе до помилки виконання.
Така помилка називається переповненням стеку (stack overflow)
Щоб цьому запобігти, інтерпретатори примусово завершують роботу програми після досягнення ліміту викликів функцій, 
які не повертають результат. Така межа називається максимальною глибиною рекурсії, або максимальним розміром стеку викликів.
У Python граничним значенням вважається 1000 викликів функцій.
'''

'''Числа Фібоначчі — це послідовність чисел, у якій кожне наступне число є сумою двох попередніх.
 Перші два числа в цій послідовності, за визначенням, дорівнюють 0 і 1.
'''

def fibonacci(n):
    if n <= 1: # базовий випадок
        return n
    else:
        return fibonacci(n-1) + fibonacci(n-2) # рекурсивний випадок

print(fibonacci(10)) # виведе 55



'''Факторіал натурального числа n (позначається як n!) —
це добуток всіх натуральних чисел від 1 до n включно.

Цей код працює тому, що кожен виклик factorial(n-1) працює на дедалі меншому числі 
аж до того моменту, коли n дорівнює 0, і відбувається базовий випадок.
На цьому етапі рекурсія завершується, і результат починає "повертатися" назад 
через усі попередні виклики функції, обчислюючи добуток на кожному кроці.'''

def factorial(n):
    if n == 0: # базовий випадок
        return 1
    else:
        return n * factorial(n-1) # рекурсивний випадок
print(factorial(5)) # виведе 120   5 * 4 * 3 * 2 * 1 = 120. 

'''Стек викликів — це специфічна частина пам'яті, яка використовується для зберігання інформації
 про активні виклики функцій. Кожен раз, коли функція викликається, створюється новий запис 
 (або "шар") у цьому стеку для цього конкретного виклику. Цей шар містить інформацію 
 про змінні функції, її параметри та місце, звідки була викликана функція, 
 щоб після завершення виконання функції програма могла продовжити роботу з правильного місця.'''

def factorial(n):
    print("Виклик функції factorial з n = ", n)
    if n == 1:
        print("Базовий випадок, n = 1, повернення 1")
        return 1
    else:
        result = n * factorial(n-1)
        print("Повернення результату для n = ", n, ": ", result)
        return result

print(factorial(5)) 





